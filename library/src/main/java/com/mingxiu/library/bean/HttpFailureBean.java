public  class HttpFailureBean {
    private String url;
    private int errorCode;
    private String msg;

    public HttpFailureBean(int errorCode, String msg, String url) {
        this.errorCode = errorCode;
        this.msg = msg;
        this.url = url;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public int getErrorCode() {
        return errorCode;
    }

    public void setErrorCode(int errorCode) {
        this.errorCode = errorCode;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }
}
